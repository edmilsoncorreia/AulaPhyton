# OPERADORES UNÁRIOS
print("\n-------------------\nOPERADORES UNÁRIOS:\n-------------------\n")
print("Exemplo número 4")
print("Operador '~': ", ~4)
print("Operador '+': ", +4)
print("Operador '-': ", -4)

# OPERADORES ARITMÉTICOS
print("\n-----------------------\nOPERADORES ARITMÉTICOS:\n-----------------------\n")
x = 7
y = 2
print(f"Exemplo números {x} e {y}")
print("Operador '-': ", x - y)
print("Operador '*': ", x * y)
print("Operador '**': ", x ** y)
print("Operador '/': ", x / y)
print("Operador '//': ", x // y)
print("Operador '%': ", x % y)
